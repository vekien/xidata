    $id_count_struct = [
        'id1' => 0,
        'count1' => 0,
        'id2' => 0,
        'count2' => 0,
        'id3' => 0,
        'count3' => 0,
        'id4' => 0,
        'count4' => 0,
        'id5' => 0,
        'count5' => 0,
        'id6' => 0,
        'count6' => 0
    ];

    $table = [
        'face' => $id_count_struct,
        'head' => $id_count_struct,
        'body' => $id_count_struct,
        'hands' => $id_count_struct,
        'legs' => $id_count_struct,
        'feet' => $id_count_struct,
        'main' => $id_count_struct,
        'sub' => $id_count_struct,
        'ranged' => $id_count_struct
    ];

    /// Copy data from $data to $table
    $offset = 0;
    foreach ($table as $slotName => &$slotData) {
        foreach ($slotData as $key => &$value) {
            // Copy data from $data to $value (adjust indexing based on your data layout)
            $value = unpack('V', pack('C*', $data[$offset], $data[$offset + 1], $data[$offset + 2], $data[$offset + 3]))[1];
            $offset += 4;
        }
    }

    return $table;
}

